#Скорость первого автомобиля Vi км/ч, второго V2 км/ч, расстояние между ними  S км.
#Определить расстояние между ними через Т часов, если автомобили удаляются друг от друга.


def calculate_distance():
    while True:
        try:
            # Запрашиваем у пользователя скорости автомобилей и расстояние
            v1 = float(input("Введите скорость первого автомобиля (км/ч): "))
            v2 = float(input("Введите скорость второго автомобиля (км/ч): "))
            s = float(input("Введите начальное расстояние между автомобилями (км): "))
            t = float(input("Введите время (часы), через которое нужно рассчитать расстояние: "))

            # Проверка на положительность значений
            if v1 < 0 or v2 < 0 or s < 0 or t < 0:
                raise ValueError("Скорости, расстояние и время должны быть неотрицательными.")

            # Вычисляем общее расстояние между автомобилями через T часов
            total_distance = s + (v1 + v2) * t

            print(f"Расстояние между автомобилями через {t} часов: {total_distance:.2f} км")
            break  # Выход из цикла после успешного выполнения

        except ValueError as e:
            print(f"Ошибка ввода: {e}. Пожалуйста, попробуйте снова.")

# Запуск функции
calculate_distance()