#Скорость первого автомобиля Vi км/ч, второго V2 км/ч, расстояние между ними  S км.
#Определить расстояние между ними через Т часов, если автомобили удаляются друг от друга.

try:
    # Ввод данных
    V1 = float(input("Введите скорость первого автомобиля (км/ч): "))
    V2 = float(input("Введите скорость второго автомобиля (км/ч): "))
    S = float(input("Введите начальное расстояние между автомобилями (км): "))
    T = float(input("Введите время (часы): "))

    # Проверка на отрицательные значения
    if V1 < 0 or V2 < 0 or S < 0 or T < 0:
        print("Ошибка ввода: Скорости, расстояние и время должны быть неотрицательными.")
    else:
        total_speed = V1 + V2

        # Расчет общего пути
        total_distance = S + (total_speed * T)

        # Вывод результата
        print(f"Расстояние между автомобилями через {T} часов: {total_distance} км")

except ValueError:
    print("Ошибка ввода: Пожалуйста, введите числовые значения.")
