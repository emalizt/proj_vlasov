#Даны два целых числа: A, B. Проверить истинность высказывания: «Справедливы
#неравенства A > 2 и B < 3».
while True:
    try:
        # Запрашиваем ввод целых чисел A и B
        A = int(input("Введите целое число A: "))
        B = int(input("Введите целое число B: "))

        # Проверяем неравенства
        if A > 2 and B < 3:
            print("Высказывание истинно: A > 2 и B < 3")
        else:
            print("Высказывание ложно: A <= 2 или B >= 3")

        # Выход из цикла после проверки
        break
    except ValueError:
        print("Ошибка: Пожалуйста, введите целые числа.")
